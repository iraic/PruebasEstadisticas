        private void varianza(int n){
                //Calculo de la media
        double suma = 0, media, var = 0;
        for (int i = 0; i < aleatorios.length; i++)
            suma += aleatorios[i];
        media = suma / n;
        //Calcular varianza
        suma = 0;
        for (int i = 0; i < aleatorios.length; i++)
            suma += Math.pow((aleatorios[i] - media),2);
        var = suma / (n-1);
        jTextField11.setText(redondear(var) + "");
        //Calculo de limites
        ChiSquaredDistribution cs = new ChiSquaredDistribution(n-1);
        double alpha = 1.0 - (Integer.parseInt(jTextField7.getText()) / 100.0);
        double alpha2 = alpha/2;
        double x = cs.inverseCumulativeProbability(alpha2);
        double li =  x / (12.0 * (n - 1));
        jTextField15.setText(redondear(li) + "");
        
        x = cs.inverseCumulativeProbability(1 - alpha2);
        double ls =  x / (12.0 * (n - 1));
        jTextField16.setText(redondear(ls) + "");
        if (li < var && var < ls){
            jLabel17.setText("Paso la prueba Wiiiiiiii");
            jLabel17.setBackground(Color.GREEN);
        }
        else{
            jLabel17.setText("No paso la prueba :'(");
            jLabel17.setBackground(Color.RED);
        }
    }
