    private void media(int n){
                //Calculo de la media
        double suma = 0, media;
        for (int i = 0; i < aleatorios.length; i++)
            suma += aleatorios[i];
        media = suma / n;
        jTextField6.setText(redondear(media) + "");
        //Calculo de limites
        NormalDistribution nd = new NormalDistribution();
        double alpha = 1.0 - (Integer.parseInt(jTextField7.getText()) / 100.0);
        double alpha2 = alpha/2;
        double z = nd.inverseCumulativeProbability(1 - alpha2);
        //lix=1/2 - Zα/2 * [1/(12*raiz(n))]
        double li =  0.5 - (z * 1.0 / (12.0 * Math.sqrt(n)));
        jTextField8.setText(redondear(li) + "");
        double ls =  0.5 + (z * 1.0 / (12.0 * Math.sqrt(n)));
        jTextField9.setText(redondear(ls) + "");
        if (li < media && media < ls){
            jLabel10.setText("Paso la prueba Wiiiiiiii");
            jLabel10.setBackground(Color.GREEN);
        }
        else{
            jLabel10.setText("No paso la prueba :'(");
            jLabel10.setBackground(Color.RED);
        }
    }
